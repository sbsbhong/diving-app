import { mkdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

import { compileFromFile } from 'json-schema-to-typescript';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const packageDir = path.resolve(__dirname, '..');
const schemaPath = path.join(packageDir, 'schemas', 'watch-sync-message.schema.json');
const outputPath = path.join(packageDir, 'generated', 'typescript', 'index.ts');

const generated = await compileFromFile(schemaPath, {
  bannerComment: '// This file is auto-generated by json-schema-to-typescript. Do not edit manually.\n',
  cwd: path.join(packageDir, 'schemas'),
  unreachableDefinitions: true
});

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(outputPath, generated, 'utf8');

console.log(`Generated TypeScript contracts: ${path.relative(packageDir, outputPath)}`);
